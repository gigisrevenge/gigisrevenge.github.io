const logoEl = document.getElementById("asciilogo");
let hoverInterval = null;

function getRandomLogo(){
    const index = Math.floor(Math.random()*asciiLogos.length);
    return asciiLogos[index];
}

function startLogoCycle(){
    if (hoverInterval) return;
    hoverInterval=setInterval(() => {
        logoEl.textContent = getRandomLogo();
    }, 150);
}

function stopLogoCycle(){
    clearInterval(hoverInterval);
    hoverInterval=null;
    logoEl.textContent = getRandomLogo();
}

window.onload = () => {
    logoEl.textContent = getRandomLogo();
}

logoEl.addEventListener("mouseenter", startLogoCycle);
logoEl.addEventListener("mouseleave", stopLogoCycle);